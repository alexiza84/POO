
package Aritmetica;
public class Aritmetica {
    public int Sumar(int op1, int op2,int op3){
    return op1+op2+op3;
    }   
    public int Restar(int op1, int op2){
    return op1-op2;
    }   
    public int Multiplicar(int op1, int op2){
    return op1*op2;
    }   
    public float Dividir(float op1, float op2){
    return op1/op2;
    }   
}
